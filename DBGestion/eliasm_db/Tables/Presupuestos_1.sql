CREATE TABLE [eliasm_db].[Presupuestos] (
    [Id]                       NVARCHAR (150)  NOT NULL,
    [Codigo]                   NVARCHAR (150)  NULL,
    [Fecha]                    DATETIME        NULL,
    [FechaVencimiento]         DATETIME        NULL,
    [ClienteId]                NVARCHAR (150)  NULL,
    [TipoResponsableId]        NVARCHAR (150)  NULL,
    [ClienteCategoriaId]       NVARCHAR (150)  NULL,
    [EstadoId]                 NVARCHAR (150)  NULL,
    [DescuentoId]              NVARCHAR (150)  NULL,
    [DescuentoPorcentaje]      NUMERIC (18, 2) NULL,
    [DescuentoUsuario]         NVARCHAR (450)  NULL,
    [DescuentoFecha]           DATETIME        NULL,
    [FechaAprobacion]          DATETIME        NULL,
    [FechaRechazo]             DATETIME        NULL,
    [MotivoAprobacionRechazo]  NVARCHAR (500)  NULL,
    [UsuarioAprobacionRechazo] NVARCHAR (450)  NULL,
    [SucursalId]               NVARCHAR (150)  NULL,
    [Estado]                   BIT             NULL,
    [FechaAlta]                DATETIME        NULL,
    [UsuarioAlta]              NVARCHAR (450)  NULL,
    [FechaEdit]                DATETIME        NULL,
    [UsuarioEdit]              NVARCHAR (450)  NULL,
    CONSTRAINT [PK_Presupuestos] PRIMARY KEY CLUSTERED ([Id] ASC),
    CONSTRAINT [FK_Presupuestos_Clientes] FOREIGN KEY ([ClienteId]) REFERENCES [eliasm_db].[Clientes] ([Id]),
    CONSTRAINT [FK_Presupuestos_ParamPresupuestosDescuentos] FOREIGN KEY ([DescuentoId]) REFERENCES [eliasm_db].[ParamPresupuestosDescuentos] ([Id]),
    CONSTRAINT [FK_Presupuestos_ParamPresupuestosEstados] FOREIGN KEY ([EstadoId]) REFERENCES [eliasm_db].[ParamPresupuestosEstados] ([Id]),
    CONSTRAINT [FK_Presupuestos_ParamTiposResponsables] FOREIGN KEY ([TipoResponsableId]) REFERENCES [eliasm_db].[ParamTiposResponsables] ([Id])
);

